CREATE TABLE EMP_POSITION(
  JOB_NO 		INT(5) NOT NULL AUTO_INCREMENT,
  JOB_NAME 		VARCHAR(10),
  PRIMARY KEY (JOB_NO)
) ;

CREATE TABLE DEPARTMENTS(
  DEPT_NO 		INT(11) NOT NULL AUTO_INCREMENT,
  DEPT_NAME 	VARCHAR(10),
  PRIMARY KEY (DEPT_NO)
) ;

