) ;

CREATE TABLE COMMENTS(
  COMMENT_NO 			INT(6) NOT NULL AUTO_INCREMENT,
  ANONY_NO 			INT(6) NOT NULL,
  EMP_NO 			INT(8) NULL,
  CREATE_DATE 		DATETIME,
  CONTENT 		VARCHAR(700),
  STATE 			INT(1),
  DEPTH 			INT(1),
  GROUP_NO 			INT DEFAULT 0,
  PRIMARY KEY (COMMENT_NO),
  FOREIGN KEY (ANONY_NO) REFERENCES `ANONY` (ANONY_NO) ON DELETE CASCADE,
  FOREIGN KEY (EMP_NO) REFERENCES `EMPLOYEES` (EMP_NO) ON DELETE SET NULL
) ;

