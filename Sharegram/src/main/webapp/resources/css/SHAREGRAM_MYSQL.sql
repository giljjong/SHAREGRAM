CREATE TABLE EMPLOYEES(
  EMP_NO 			INT(8) NOT NULL UNIQUE,
  EMP_PW 			VARCHAR(64),
  NAME 				VARCHAR(50),
  BIRTHYEAR 		VARCHAR(4),
  BIRTHDAY 			VARCHAR(4),
  JOIN_DATE 		DATETIME,
  PROF_IMG			VARCHAR(100),
  INFO_MODIFY_DATE 	DATETIME,
  STATUS 			VARCHAR(10),
  SALARY 			INT(11),
  JOB_NO 			INT(5),
  DEPT_NO 			INT(5),
  PHONE				VARCHAR(11) NOT NULL UNIQUE,
  SESSION_ID 		VARCHAR(32),        -- 추가
  SESSION_LIMIT_DATE DATETIME,             -- 추가

  PRIMARY KEY (EMP_NO),
  FOREIGN KEY (JOB_NO) REFERENCES `EMP_POSITION` (JOB_NO) ON DELETE SET NULL,
  FOREIGN KEY (DEPT_NO) REFERENCES `DEPARTMENTS` (DEPT_NO) ON DELETE SET NULL
) ;

CREATE TABLE RETIRED(
  EMP_NO 			INT(8),
  NAME 				VARCHAR(50),
  PHONE 			VARCHAR(11),
  BIRTHYEAR 		VARCHAR(4),
  BIRTHDAY 			VARCHAR(4),
  JOIN_DATE 		DATETIME,
  RETIRED_DATE 		DATETIME,
  JOB_NO 			INT(8),
  DEPT_NO 			INT(8),
  PRIMARY KEY (EMP_NO)
) ;

CREATE TABLE SCHEDULE(
  SCHEDULE_NO 				INT(8) NOT NULL AUTO_INCREMENT,
  EMP_NO 					INT(8),
  SCHEDULE_TITLE 			VARCHAR(100),
  SCHEDULE_START 			VARCHAR(40),
  SCHEDULE_END 				VARCHAR(40),
  SCHEDULE_ALLDAY 			VARCHAR(20),
  PRIMARY KEY (SCHEDULE_NO),
  FOREIGN KEY (EMP_NO) REFERENCES `EMPLOYEES` (EMP_NO) ON DELETE CASCADE
) ;

CREATE TABLE ATTENDANCE(
  ATT_NO 			INT(8) NOT NULL AUTO_INCREMENT,
  EMP_NO 			INT(8),
  ATT_START 		VARCHAR(40),
  ATT_END 			VARCHAR(40),
  EARLY_STATUS 		VARCHAR(15),
  ATT_STATUS 		VARCHAR(40),
  PRIMARY KEY (ATT_NO),
  FOREIGN KEY (EMP_NO) REFERENCES `EMPLOYEES` (EMP_NO) ON DELETE CASCADE
) ;

CREATE TABLE EMP_ADDR(
  EMP_ADDR_NO 	INT(8) NOT NULL AUTO_INCREMENT,
  EMP_NO 		INT(8),
  DEPT_NO 		INT(8),
  JOB_NO 		INT(8),
  NAME 			VARCHAR(50),
  PASSWORD 		VARCHAR(20),
  PHONE 		VARCHAR(16),
  EMAIL 		VARCHAR(100),
  FAX 			VARCHAR(20),
  PRIMARY KEY (EMP_ADDR_NO),
  FOREIGN KEY (EMP_NO) REFERENCES `EMPLOYEES` (EMP_NO) ON DELETE CASCADE,
  FOREIGN KEY (DEPT_NO) REFERENCES `DEPARTMENTS` (DEPT_NO) ON DELETE SET NULL,
  FOREIGN KEY (JOB_NO) REFERENCES `EMP_POSITION` (JOB_NO) ON DELETE SET NULL
) ;

CREATE TABLE ADDR_GROUP(
  ADDR_GROUP_NO 	INT(8) NOT NULL AUTO_INCREMENT,
  EMP_NO 			INT(8) NOT NULL,
  ADDR_GROUP_NAME 	VARCHAR(36),
  PRIMARY KEY (ADDR_GROUP_NO),
  FOREIGN KEY (EMP_NO) REFERENCES `EMPLOYEES` (EMP_NO) ON DELETE CASCADE
) ;

CREATE TABLE PERSONAL_ADDR(
  PERSONAL_NO 		INT(8) NOT NULL AUTO_INCREMENT,
  ADDR_GROUP_NO 	INT(8) NOT NULL,
  ADDR_NAME 		VARCHAR(50),
  ADDR_PHONE 		VARCHAR(16),
  COMPANY 			VARCHAR(50),
  EMAIL 			VARCHAR(100),
  FAX 				VARCHAR(20),
  MEMO 				VARCHAR(1000),
  IMPORTANT_CHECK 	VARCHAR(1),
  PRIMARY KEY (PERSONAL_NO),
  FOREIGN KEY (ADDR_GROUP_NO) REFERENCES `ADDR_GROUP` (ADDR_GROUP_NO) ON DELETE CASCADE
) ;

