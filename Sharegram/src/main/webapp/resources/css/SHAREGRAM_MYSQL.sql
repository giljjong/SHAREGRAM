CREATE TABLE MAIL(
  MAIL_NO 		INT(6) NOT NULL AUTO_INCREMENT,
  EMP_NO 		INT(8) NOT NULL,
  SENDER 		VARCHAR(100),
  SUBJECT 		VARCHAR(100),
  MAIL_CONTENT 	LONG,
  SEND_DATE 	DATETIME,
  SEND_STATUS 	VARCHAR(30),
  TAG_NO 		INT,
  PRIMARY KEY (MAIL_NO),
  FOREIGN KEY (EMP_NO) REFERENCES `EMPLOYEES` (EMP_NO) ON DELETE CASCADE
) ;

CREATE TABLE RECEIVERS(
